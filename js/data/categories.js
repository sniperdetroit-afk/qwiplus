/* =========================
   RENDER CATEGORIES
========================= */
export function renderCategories() {


  return `
    <section class="categories-grid">
      ${categories.map(cat => `
        <button 
          class="category-card"
          data-view="subcategories"
          data-category="${cat.id}"
        >
          <img src="${images[cat.id]}" loading="lazy" />
          <div class="category-overlay"></div>
          <span>${cat.name}</span>
        </button>
      `).join("")}
    </section>
  `;
}

/* =========================
   SUBCATEGORIES
========================= */

export function renderSubcategories(state) {

  const category = categories.find(c => c.id === state.category);

  if (!category) return `<p>Error categoría</p>`;

  return `
    <section class="subcategories">

      <button class="back-btn" data-view="categories">
        ← Volver
      </button>

      <h3>${category.name}</h3>

      <div class="subcategory-grid">
        ${category.sub.map(sub => {

          if (typeof sub === "object" && sub.sub) {
            return `
              <button 
                class="subcategory-item"
                data-view="subsubcategories"
                data-category="${category.id}"
                data-subcategory="${sub.name}"
              >
                ${sub.name}
              </button>
            `;
          }

          return `
            <button class="subcategory-item">
              ${typeof sub === "object" ? sub.name : sub}
            </button>
          `;

        }).join("")}
      </div>

    </section>
  `;
}


/* =========================
   SUBSUBCATEGORIES
========================= */

export function renderSubSubcategories(state) {

  const category = categories.find(c => c.id === state.category);
  if (!category) return `<p>Error categoría</p>`;

  const sub = category.sub.find(
    s => typeof s === "object" && s.name === state.subcategory
  );

  if (!sub || !sub.sub) return `<p>No hay más niveles</p>`;

  return `
    <section class="subsubcategories">

      <button 
        class="back-btn"
        data-view="subcategories"
        data-category="${category.id}"
      >
        ← Volver
      </button>

      <h3>${sub.name}</h3>

      <div class="subcategory-grid">
        ${sub.sub.map(item => `
          <button class="subcategory-item">
            ${item}
          </button>
        `).join("")}
      </div>

    </section>
  `;
}
